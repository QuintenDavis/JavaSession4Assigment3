
public class Employee {

	private String name;
	private String idNUM;
	private int age;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getIdNUM() {
		return idNUM;
	}
	public void setIdNUM(String idNUM) {
		this.idNUM = idNUM;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
}
